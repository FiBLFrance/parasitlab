      <h5 class="my-1">Des antiparasitaires insecticides</h5>
      Parmi les produits antiparasitaires utilisés couramment, certains présentent aussi des propriétés insecticides.
      C’est le cas des ivermectines, plus connues sous leurs noms commerciaux : <span style="font-variant:small-caps">ivomec nd, dectomax nd, eprinex nd</span>, etc.
      pour les ruminants, <span style="font-variant:small-caps">eqvalan nd, eraquell nd, hippomectin nd</span> pour les chevaux.
      <br>Administrées aux animaux, les ivermectines se retrouvent sous forme active dans les fèces et persistent plusieurs semaines dans le milieu extérieur.
    </div>

  </li>

  <li class="media my-3  ">

    <div class="media-body mr-3">

      <h5>Qui s'attaquent aussi à la faune auxiliaire</h5>

      Ces molécules sont ainsi responsables de la mort de 90% des larves de bousier la première semaine qui suit le traitement.
      Et cet effet persiste encore la semaine suivante. </br>Même à doses résiduelles, les ivermectines continuent à perturber les bousiers adultes.
      <br>D’autres insectes ( papillons, abeilles) attirés par les déjections, subissent l’effet insecticide des ivermectines.
    </div>

    <img src="{{ url('storage/img/coprophage_1.png') }}" alt="Coprophage">

  </li>

  <li class="media">

    <img src="{{ url('storage/img/paturage.png') }}" alt="Antiparasitaires">

    <div class="media-body ml-3">

      <h5 class="my-1">Et entraînent une dégradation des pâturages</h5>
      Or, le traitement rapide des déjections par les bousiers adultes conduit à améliorer la qualité des sols.
      S’il n’y a plus de bousier, les déjections ne sont pas enfouies et se minéralisent.
      Elles peuvent rester intactes plusieurs années à la surface du sol, réduisant ainsi la surface utile du pâturage
      et favorisant à terme l'apparition de refus. En cas de fauche, des morceaux de bouses non dégradés
      peuvent également se retrouver dans le foin qui devient moins appétant.
    </br>A terme, on observe une dégradation de la qualité des pâturages.
  </div>

</li>

</ul>

<p class="lead">Pour toutes ces raisons, l’usage des ivermectines devrait être évité durant les périodes de pâturage.
 En dehors de ces périodes, le recours aux <i></i>vermectines devrait se limiter aux animaux trop infestés.</p>

