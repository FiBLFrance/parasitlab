<h3>
  Protection des données personnelles
</h3>
<p>
  Les données personnelles suivantes apparaissent sur votre compte utilisateur de Parasit’Lab : raison sociale, nom, adresse postale, email, numéro de téléphone, numéro EDE (compte éleveur), coordonnées de votre vétérinaire (compte éleveur/détenteur), résultats d’analyse, informations sur vos animaux (compte éleveur/détenteur).
</p>
<p>
  Ces données personnelles sont utilisées par notre service afin de
</p>
<ul>
  <li>Réaliser les analyses demandées,</li>
  <li>Vous transmettre les résultats, ainsi qu’au vétérinaire (dans le cas où vous avez renseigné ses coordonnées),</li>
  <li>Vous envoyer des informations sur nos activités,</li>
  <li>Vous contacter dans le cadre d’un essai,</li>
  <li>Réaliser des statistiques sur le parasitisme après anonymisation.</li>
</ul>

<p>
  Ces données ne seront pas transmises à des tiers sans votre autorisation.
</p>
<p>
  Si vous refusez ces conditions d’utilisation de vos données personnelles,
    <a class="btn btn-light" href="mailto:contac@parasitlab.org?subject=Refus des conditions de protection des données personnelles">cliquez ici</a>
</p>
<p>
  Si vous refusez la création de votre compte utilisateur,
  <a class="btn btn-light" href="mailto:contac@parasitlab.org?subject=Demande de suppression de mon compte utilisateur">cliquez ici</a>

</p>
